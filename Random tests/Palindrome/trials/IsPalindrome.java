package Palindrome.trials;

public class IsPalindrome {
    static void IsEPalindrome(char[] e){
        for(char item:e){
            System.out.print(item);
        }
        System.out.println();
    }
    static void IsOPalindrome(char[] o){
        for(char item:o){
            System.out.print(item);
        }
        System.out.println();
    }
}
